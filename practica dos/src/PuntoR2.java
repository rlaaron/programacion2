public class PuntoR2 {
    private int x;
    private int y;

    public PuntoR2(){
        this.x=0;
        this.y=0;
    }

    public PuntoR2(int x, int y){
        this.x = x;
        this.y = y;
    }

    // getters
    public int getPx(){
        return x;
    }

    public int getPy(){
        return y;
    }
    
    //setters
    public void setPx(int x){
        this.x = x;
    }

    public void setPy(int y){
        this.x = y;
    }

    
}
